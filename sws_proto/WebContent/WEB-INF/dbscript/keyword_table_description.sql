- 키워드별 테이블이름 설명 -

* 키워드 A-Z || a-z 까지는 = '첫글자 + keyword' 이다.
  ex) - 'apple' 키워드는 akeyword 테이블에 Insert 된다.

* 키워드 한글 한글자or자음 까지는 -> 가-힣 || ㄱ-ㅎ 까지 = 첫글자 발음 그대로 영어로 + keyword이다.  즉, 순서대로 

  {"ga", "gga", "na", "da", "dda", "ra", "ma", "ba", "bba", "sa", "ssa", "aa", "ja", "jja", "cha", "ka", "ta", "pa", "ha"}; + 'keyword' 이다.
  ex) - '과자' 키워드는 gakeyword 테이블에 Insert 된다.
  
