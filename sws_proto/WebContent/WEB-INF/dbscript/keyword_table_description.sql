* 키워드 한글 모음 ㅏ-ㅣ 까지 = 'vowel' + 'keyword' 이다. 
  ex) - 'ㅑ' 키워드는 vowelkeyword 테이블에 Insert 된다.

* 키워드 숫자는 0-9까지 = 숫자를 영어로 + 'keyword' 이다.
  ex) - '3' 키워드는 threekeyword 테이블에 Insert 된다. 
  
* 키워드 특수문자 = speicalletter + 'keyword' 이다.
  ex) - '#' 키워드는 speicalletterkeyword 테이블에 Insert 된다.
  
 
  
